package com.openthinks.libs.utilities.json.support;

/**
 * JSON token constant
 * 
 * @author dailey
 *
 */
public interface JSONToken {
  char LBRACE = '{';
  char RBRACE = '}';
  char LBRACKET = '[';
  char RBRACKET = ']';
  char COMMA = ',';
  char COLON = ':';
  char DOUBLE_QUOTE = '\"';
  char SINGLE_QUOTE = '\'';

  char ESCAPE = '\\';
}
